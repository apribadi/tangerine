pub(crate) const BACKEND: Backend = Backend::Basic;

pub(crate) struct HashU8(&'static [[u8; 256]; 2], u8, u8);

impl Hash<u8> for HashU8 {
  #[inline(always)]
  fn new(g: &mut impl Rng) -> Self {
    let a = 1 | g.next_u32() as u8;
    let b = invert_u8(a);
    Self(&HASH_U8_PERM, a, b)
  }

  #[inline(always)]
  fn new_nondet() -> Self {
    let a = 1 | dandelion::thread_local::u32() as u8;
    let b = invert_u8(a);
    Self(&HASH_U8_PERM, a, b)
  }

  #[inline(always)]
  fn forward(&self) -> impl Copy + Fn(u8) -> u8 {
    let p = &self.0[0];
    let m = self.1;
    move |x| {
      let x = p[x as usize];
      let x = x.wrapping_mul(m).wrapping_sub(1);
      x
    }
  }

  #[inline(always)]
  fn reverse(&self) -> impl Copy + Fn(u8) -> u8 {
    let p = &self.0[1];
    let m = self.2;
    move |x| {
      let x = x.wrapping_mul(m).wrapping_add(m);
      let x = p[x as usize];
      x
    }
  }
}

pub(crate) struct HashU32((u32, u32), (u32, u32));

impl Hash<u32> for HashU32 {
  #[inline(always)]
  fn new(g: &mut impl Rng) -> Self {
    let n = g.next_u64();
    let a = 1 | (n as u32);
    let b = 1 | ((n >> 32) as u32);
    let x = invert_u32(a.wrapping_mul(b));
    let c = x.wrapping_mul(a);
    let d = x.wrapping_mul(b);
    Self((a, b), (c, d))
  }

  #[inline(always)]
  fn new_nondet() -> Self {
    let n = dandelion::thread_local::u64();
    let a = 1 | (n as u32);
    let b = 1 | ((n >> 32) as u32);
    let x = invert_u32(a.wrapping_mul(b));
    let c = x.wrapping_mul(a);
    let d = x.wrapping_mul(b);
    Self((a, b), (c, d))
  }

  #[inline(always)]
  fn forward(&self) -> impl Copy + Fn(u32) -> u32 {
    let a = self.0.0;
    let b = self.0.1;
    move |x| {
      let x = x ^ x.rotate_left(7) ^ x.rotate_left(23);
      let x = x.wrapping_mul(a);
      let x = x.swap_bytes();
      let x = x.wrapping_mul(b).wrapping_sub(1);
      x
    }
  }

  #[inline(always)]
  fn reverse(&self) -> impl Copy + Fn(u32) -> u32 {
    let c = self.1.0;
    let d = self.1.1;
    move |x| {
      let x = x.wrapping_mul(c).wrapping_add(c);
      let x = x.swap_bytes();
      let x = x.wrapping_mul(d);
      let x = x ^ x.rotate_left(7) ^ x.rotate_left(23);
      x
    }
  }
}

